#include <unistd.h>

void 	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(unsigned int sum)
{
	if (sum > 9)
		ft_putnbr(sum / 10);
	ft_putchar((sum % 10) + 48);
}

int	main(int argc, char **argv)
{
	(void)argv;

	ft_putnbr(argc - 1);
	write(1, "\n", 1);
	return (0);
}
